print("Витязь на распутье. На камне надпись:")
print("1 — Направо: коня потеряешь, себя спасёшь")
print("2 — Налево: себя потеряешь, коня спасёшь")
print("3 — Прямо: и себя, и коня потеряешь")

choice = input("\nВыберите путь (1/2/3): ").strip()

if choice == "1":
    print("Витязь пошёл направо. Конь погиб, но витязь выжил и двинулся дальше.")
elif choice == "2":
    print("Витязь избрал левый путь. Он погиб, но конь уцелел.")
elif choice == "3" or choice.lower() in ["3", "прямо", "вперёд"]:
    print("Витязь смело пошёл прямо. В битве потерял коня, был тяжело ранен, но победил врага.")
else:
    print("Витязь так и не решился. Время ушло, опасность настигла его врасплох.")

print("Выбор сделан. Судьба свершилась.")
